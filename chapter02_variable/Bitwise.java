// here we will try bitwise operation

public class Bitwise {
	public static void main(String[] args) {
		boolean a = true;
		boolean b = false;
		System.out.println(a & b);
		System.out.println(a | b);
		System.out.println(a ^ b);
		

		int c = 10;
		System.out.println(c);
		System.out.println(c >> 2);
		System.out.println(c << 2);
		

	}
}