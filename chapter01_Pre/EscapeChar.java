public class EscapeChar {
	public static void main(String[] args) {
		System.out.println("书名\t作者\t价格\t销量\r\n三国\t罗贯中\t120\t1000");

		// \n change line
		System.out.println("2021\n2022\n2023");

		// \t table mark
		System.out.println("Peter\tMary\tOsbon");

		// \\ sinle \

		System.out.println("C:\\Windows\\User\\Chao\\Desktop");

		// \"

		System.out.println("Chao, I just want to tell you: \"Don't ever give up !\"");

		// \r Enter key
		System.out.println("I really love \ryou!");
	}
}


