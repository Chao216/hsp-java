package com.study.Math_;

public class math_metho {
    public static void main(String[] args) {
        System.out.println(Math.sqrt(9));
        System.out.println(Math.pow(2,3));//2**3 = 8
        System.out.println(Math.max(10,0.32));
        System.out.println(Math.ceil(10.23));// --> 11
        System.out.println(Math.floor(10.97)); // --> 10
        System.out.println(Math.log(23.1));
        System.out.println(Math.log10(100));
        System.out.println(Math.PI);
        System.out.println(Math.E);

    }
}
